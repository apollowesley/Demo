package com.kyl.ps.exception;

public class NotDataException extends Exception {

	/** serialVersionUID*/
	private static final long serialVersionUID = 2005822079140637512L;

	public NotDataException() {
		super();
	}

	public NotDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotDataException(String message) {
		super(message);
	}

	public NotDataException(Throwable cause) {
		super(cause);
	}

}

