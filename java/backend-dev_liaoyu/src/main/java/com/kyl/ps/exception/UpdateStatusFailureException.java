package com.kyl.ps.exception;

public class UpdateStatusFailureException extends Exception {

	/** serialVersionUID*/
	private static final long serialVersionUID = 2005822079140637512L;

	public UpdateStatusFailureException() {
		super();
	}

	public UpdateStatusFailureException(String message, Throwable cause) {
		super(message, cause);
	}

	public UpdateStatusFailureException(String message) {
		super(message);
	}

	public UpdateStatusFailureException(Throwable cause) {
		super(cause);
	}

	
	
}

