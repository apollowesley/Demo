package com.kyl.ps.exception;

public class NotLoginException extends Exception {

	/** serialVersionUID*/
	private static final long serialVersionUID = -1494162008412138236L;

	public NotLoginException() {
		super();
	}

	public NotLoginException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotLoginException(String arg0) {
		super(arg0);
	}

	public NotLoginException(Throwable arg0) {
		super(arg0);
	}

	
	
}

